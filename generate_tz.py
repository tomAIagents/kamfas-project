COLOR_DARK   = (31, 56, 100)
COLOR_ACCENT = (197, 90, 17)
COLOR_GREEN  = (32, 120, 60)
COLOR_RED    = (180, 30, 30)
COLOR_GRAY   = (100, 100, 100)

def shade_cell(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)

def add_heading(doc, text, level, color=COLOR_DARK):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.color.rgb = RGBColor(*color)
    return p

def add_paragraph(doc, text='', bold=False, italic=False, size=11, color=None, indent=0):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*color)
    if indent:
        p.paragraph_format.left_indent = Cm(indent)
    return p

def add_bullet(doc, text, bold_prefix=None, indent=0, color=None):
    p = doc.add_paragraph(style='List Bullet')
    if bold_prefix:
        r = p.add_run(bold_prefix)
        r.bold = True
        if color:
            r.font.color.rgb = RGBColor(*color)
    r2 = p.add_run(text)
    r2.font.size = Pt(10.5)
    p.paragraph_format.left_indent = Cm(indent)
    p.paragraph_format.space_after = Pt(2)
    return p

def add_table(doc, headers, rows, col_widths=None, header_color='1F3864'):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    hdr_row = table.rows[0]
    for i, h in enumerate(headers):
        cell = hdr_row.cells[i]
        cell.text = h
        shade_cell(cell, header_color)
        for para in cell.paragraphs:
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in para.runs:
                run.bold = True
                run.font.color.rgb = RGBColor(255, 255, 255)
                run.font.size = Pt(10)
    for r_idx, row_data in enumerate(rows):
        row = table.rows[r_idx + 1]
        fill = 'EEF2F7' if r_idx % 2 == 0 else 'FFFFFF'
        for c_idx, val in enumerate(row_data):
            cell = row.cells[c_idx]
            cell.text = str(val)
            shade_cell(cell, fill)
            for para in cell.paragraphs:
                for run in para.runs:
                    run.font.size = Pt(10)
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = Cm(w)
    return table

def separator(doc):
    p = doc.add_paragraph('─' * 90)
    for run in p.runs:
        run.font.color.rgb = RGBColor(180, 180, 180)
        run.font.size = Pt(7)
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after = Pt(4)

def note(doc, text, kind='info'):
    colors = {'info': 'DCE6F1', 'warn': 'FFF2CC', 'danger': 'FFE0E0', 'good': 'E2EFDA'}
    p = doc.add_paragraph()
    shade_cell_map = {'info': COLOR_DARK, 'warn': COLOR_ACCENT, 'danger': COLOR_RED, 'good': COLOR_GREEN}
    run = p.add_run(f'  {text}  ')
    run.font.size = Pt(10)
    run.font.color.rgb = RGBColor(*shade_cell_map[kind])
    run.italic = True
    p.paragraph_format.left_indent = Cm(0.5)
    p.paragraph_format.space_after = Pt(6)

